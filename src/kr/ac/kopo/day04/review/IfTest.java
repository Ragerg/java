package kr.ac.kopo.day04.review;

public class IfTest {
	
	public static void main(String[] args) {
		
		int i = 10;
		int j = 9;
		
		if (i > j) {
			System.out.println("i가 j보다 크다.");
		}
		
		if (i == j ) {
			System.out.println("i와 j가 같다.");
		}
		
		if (i < j) {
			System.out.println("i가 j보다 작다.");
		}
		
	}

}
